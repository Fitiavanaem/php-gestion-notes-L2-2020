            	    <p style=" font-weight: bold; font-size: 22px;text-align: center;">L'absence de connexions inter-réseaux</p>
    	            <p style="text-indent: 1.5cm;">Avant la propagation des connexions inter-réseaux qui amena l’Internet actuel, la plupart des réseaux de communication étaient limités à des communications entre les postes d'un même réseau. Quelques réseaux avaient des passerelles ou des ponts les reliant entre eux, mais la plupart du temps ils étaient limités ou conçus pour un usage unique. Une méthode déjà utilisée dans les réseaux de télécommunication reposait sur un ordinateur central raccordé à ses terminaux via de longues lignes. Cette méthode fut utilisée dans les années 1950 par le projet RAND afin de permettre la collaboration de chercheurs tels qu'Herbert Simon, alors situé à Pittsburgh en Pennsylvanie, et les chercheurs de Santa Monica en Californie, tous travaillant sur la démonstration assistée par ordinateur et l'intelligence artificielle6.</p>
    	            <p>Trois terminaux et une agence du département de la Défense des États-Unis[modifier | modifier le code]</p>
    	            <p style="text-indent: 1.5cm;">Un pionnier important dans l'histoire du réseau mondial, J.C.R. Licklider, mit en avant l'idée, dans sa publication de janvier 1960, Man-Computer Symbiosis (« La symbiose homme-ordinateur ») :</p>
    	            <p>« un réseau de tels [ordinateurs], connectés les uns aux autres par des lignes de télécommunications large bande » qui fournissait « les fonctions de bibliothèques actuelles couplées avec les avancées faites dans le stockage et la récupération d'informations et [d'autres] fonctions symbiotiques.» — J.C.R. Licklider7</p>
             	    <p style="font-weight: bold;text-indent: 1.5cm;">En octobre 1962, J.C.R. Licklider fut promu à la tête du bureau de traitement de l'information de la Defense Advanced Research Projects Agency (DARPA ou ARPA) sous tutelle du Département de la Défense des États-Unis, et forma un groupe informel à l'intérieur de la DARPA afin de développer la recherche informatique. Trois terminaux furent installés sous la tutelle du bureau de traitement de l'information. Un pour System Development Corporation à Santa Monica, Californie, un pour Project Genie à l'université de Californie à Berkeley et un pour le projet Multics à l'Institut de technologie du Massachusetts (MIT). De par les problèmes rencontrés, les besoins de création d'inter-réseaux de J.C.R. Licklider devinrent alors évidents :</p>
    	            <p>Pour chacun de ces trois terminaux, j'avais trois jeux différents de commandes. Si bien que si j'étais en train de parler en direct avec quelqu'un chez SDC et que je voulais discuter de ça avec quelqu'un que je connaissais à Berkeley ou au MIT, il fallait que je me lève de devant le terminal S.D.C., que j'aille m'enregistrer sur l'autre terminal afin d'entrer en contact avec eux.</p>
            	    <p style=" font-weight: bold;text-indent: 1.5cm;">Je me suis dit, hé, mec, ce qu'il me reste à faire est évident : au lieu d'avoir ces trois terminaux, il nous faut un terminal qui va partout où tu veux et où il existe un ordinateur interactif. Cette idée était l'ARPAnet. — Robert Taylor, coauteur avec J.C.R. Licklider de The Computer as a Communications Device, dans un entretien avec le New York Times8.</p>
