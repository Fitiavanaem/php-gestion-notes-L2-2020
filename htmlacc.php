                      <p>Au cœur du problème de connexion inter-réseau résidait la question de connecter plusieurs réseaux physiquement séparés pour ne former qu'un seul réseau logique. Au cours des années 1960, plusieurs groupes ont travaillé sur l'élaboration de la commutation de paquets (packet switching en anglais). Donald Davies (National Physical Laboratory), Paul Baran (Research and Development RAND Corporation) et Leonard Kleinrock (Massachusetts Institute of Technology) se sont vu attribuer l'invention simultanément.</p>
